package prueba;

public class Auto {

	private Integer puerta;
	private Double costo;
	
	public Auto(Integer puerta, Double costo) {
		super();
		this.puerta = puerta;
		this.costo = costo;
	}

	public Integer getPuerta() {
		return puerta;
	}

	public void setPuerta(Integer puerta) {
		this.puerta = puerta;
	}

	public Double getCosto() {
		return costo;
	}

	public void setCosto(Double costo) {
		this.costo = costo;
	}
	
	
}
